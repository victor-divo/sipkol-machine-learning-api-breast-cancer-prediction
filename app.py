import streamlit as st
import requests

st.set_page_config(
    page_title="SVM Prediction",
    layout="wide"
)

st.title("Breast Cancer Prediction")

st.write("Input 30 Feature")

inputs=[]

for i in range(30):

    value=st.number_input(f"Feature {i+1}")

    inputs.append(value)

if st.button("Predict"):

    url="https://communal-reindeer-stirring.ngrok-free.app/predict"

    response=requests.post(

        url,

        json={
            "features":inputs
        }

    )

    hasil=response.json()

    st.success(f"Prediction : {hasil['prediction']}")

    st.metric("Probability",f"{hasil['probability']:.2%}")